package classTest;

public class MyMath {
	long x;
	long y;
	long add() {
		return x+y;
	}
	
	long sub() {
		return x-y;
	}
	
	long mul() {
		return x*y;
	}
	
	double div() {
		return x/y;
	}
	
	static long add(long x,long y) {
		return x+y;
	}
	
	static long sub(long x,long y) {
		return x-y;
	}
	
	static long mul(long x,long y) {
		return x*y;
	}
	
	static double div(long x,long y) {
		return x/y;
	}
}
