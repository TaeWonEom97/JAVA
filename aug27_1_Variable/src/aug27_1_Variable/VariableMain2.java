package aug27_1_Variable;

public class VariableMain2 {

	public static void main(String[] args) {
		String name;
		name = "김밥천국";
		System.out.println("레스토랑 이름: "+name);
		
		double size;
		size=100.386;
		System.out.println("평수 : "+size+" 평");
		
		int table;
		table = 20;
		System.out.println("테이블 개수 : "+table+"개");
		
		boolean work=true;
		System.out.println(work);
	}

}
